    private void nextParamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nextParamMouseClicked
        // TODO add your handling code here:
        if (countQues < numOfQues) {
            JOptionPane.showMessageDialog(DictionaryForm.this, "Please complete all the questions.");
        } else {
            countQues = 0;
            count++;
            if (count < MAX) {
                if (levelType.equals("b1")) {
                    numOfQues = gProcess.getQuAandAswB1().get(count).size();
                    setParameter(count, levelType);
                    setQuAndA(count, countQues, levelType);
                } else if (levelType.equals("b2")) {
                    numOfQues = gProcess.getQuAandAswB2().get(count).size();
                    setParameter(count, levelType);
                    setQuAndA(count, countQues, levelType);
                } else if (levelType.equals("c1")) {
                    numOfQues = gProcess.getQuAandAswC1().get(count).size();
                    setParameter(count, levelType);
                    setQuAndA(count, countQues, levelType);
                } else {
                    numOfQues = gProcess.getQuAandAswC2().get(count).size();
                    setParameter(count, levelType);
                    setQuAndA(count, countQues, levelType);
                }
            } else {
                JOptionPane.showMessageDialog(DictionaryForm.this, "", "CONGRATULATION", JOptionPane.INFORMATION_MESSAGE, new ImageIcon("C:/Users/ADMIN/DictionaryApp/src/resources/com/dictionary/resources/icon/congratulation.png"));
            }
        }
    }//GEN-LAST:event_nextParamMouseClicked

    private void nextQuesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nextQuesMouseClicked
        // TODO add your handling code here:
        if (checkAswer().equals("f")) {
            JOptionPane.showMessageDialog(DictionaryForm.this, "You haven't selected an answer yet. Please select");
        } else {
            if (checkAswer().equals(anSwer)) {
                score += 10;
            }
            countQues++;
            if (countQues < numOfQues && countQues >= 0) {

                setQuAndA(count, countQues, levelType);
            } else {
                JOptionPane.showMessageDialog(DictionaryForm.this, "End of questions");
            }
        }
        sCore.setText("score: " + score);
    }//GEN-LAST:event_nextQuesMouseClicked

    private void preQuesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_preQuesMouseClicked
        // TODO add your handling code here:
        countQues--;
        if (countQues >= 0 && countQues < numOfQues) {
            setQuAndA(count, countQues, levelType);
            if (checkAswer().equals(anSwer)) {
                score -= 10;
            }
        } else {
            JOptionPane.showMessageDialog(DictionaryForm.this, "End of questions");
        }
    }//GEN-LAST:event_preQuesMouseClicked
    
