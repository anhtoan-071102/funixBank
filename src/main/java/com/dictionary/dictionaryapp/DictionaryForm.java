    /**
     * dịch anh -việt
     * @param evt 
     */
    private void en2VietMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_en2VietMouseClicked
        // TODO add your handling code here:
        String word = jTextIn.getText().trim();
        if (!word.isEmpty()) {
            String out = dProcessEV.getMeaning(word);
            if (out != null) {
                jTextOut.setText(out);
                if (listModel.size() > 9) {
                    historyWord.remove(listModel.getElementAt(9));
                    listModel.remove(9);
                }
                if (!historyWord.containsKey(word)) {
                    historyWord.put(word, out);
                    listModel.addElement(word);
                } 
            } else {
                try {
                    jTextOut.setText(APIprocess.ggTranslateAPI(word, "vi"));
                } catch (Exception ex) {
                    Logger.getLogger(DictionaryForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(DictionaryForm.this, "Please enter a word to search.", "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_en2VietMouseClicked

